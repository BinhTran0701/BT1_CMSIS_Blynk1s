void GPIO_Init()
{
	/* GPIO Ports Clock Enable */
	RCC->AHB1ENR |= RCC_AHB1ENR_GPIOIEN_Msk;

	GPIOI->MODER = 1<<2; 	// OUTPUT
	GPIOI->OTYPER =  0;		// OUTPUT PUSH-PULL
	GPIOI->OSPEEDR = 0;		// OUTPUT SPEED LOW
	GPIOI->PUPDR = 0;		// NO PULL
}

void SysTick_Init(uint32_t TickLoad)
{
  SysTick->VAL = 0;
  SysTick->LOAD = TickLoad-1;
  SysTick->CTRL = (SysTick_CTRL_ENABLE_Msk|SysTick_CTRL_CLKSOURCE_Msk|SysTick_CTRL_TICKINT_Msk);
}


